// contoh pengkodisian menggunakan if_else pada sebuah alert confirm
var tes = confirm ("kamu yakin ?");
// baris diatas adalah variable alert confirm
if ( tes === true ) {
   // baris di atas adalah statment dalam kondisi
   alert ("user menekan OK !");
   // hasil statement jika nilai yang di kembalikan adalah true
} else {
   alert ("user menekan CANCEL !");
   //  hasil statement jika nilai yang di kembalikan adalah false
}