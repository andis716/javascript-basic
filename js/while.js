//  contoh menggunakan looping dengan while
var nilaiAwal = 1;
//  baris diatas adalah variable
while ( nilaiAwal <= 10 ) {
// baris diatas adalah statment dalam kondisi
   console.log (" Hello World " + nilaiAwal + "x");
   nilaiAwal++ ;
// baris diatas adalah increment / jumlah dari kondisi 
}
// maka looping akan berjalan sebanyak 5x

